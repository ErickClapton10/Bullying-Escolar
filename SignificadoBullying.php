				O Bullying tem origem inglesa e tem como significado o ato de ameaçar, agredir ou intimidar alguém. Segundo um levantamento, Cerca de 10% dos alunos afirmam sofrer agressões verbais ou físicas frequentes na escola, sem denunciar, é claro, possíveis casos que prefiram esconder informações sensíveis por motivos pessoais ou até medo de contar as seus familiares ou a uma pessoa de confiança da vítima. O bullying é uma violência recorrente tanto física quanto moral. Ele pode ocorrer em qualquer lugar, mas acontece com mais frequência nas escolas. As principais vítimas são crianças e adolescentes que apresentam alguma característica mais específica como ser calado ou estar acima do peso. Quem pratica o bullying busca se destacar ridicularizando a vítima. Já quem sofre o bullying acaba sofrendo fisicamente e psicologicamente. O bullying consiste em bater, zombar e ridicularizar e geralmente é feito com vítimas que não conseguem se defender. Essa violência pode ser executada por um ou mais indivíduos, ou seja, um grupo de pessoas que provocam intimidação, humilhação e até agressões nas vítimas. Ainda não existe uma lei brasileira específica que puna os agressores com o devido merecimento, mas a Justiça do Brasil está enquadrando o bullying no código penal como injúria, difamação e lesão corporal. O bullying na escola é um problema crônico. Entre os alunos as formas de agressões são variadas e ocorrem em todas as fases, desde o primário até anos superiores. O bullying acaba atrapalhando o desempenho da vítima no ambiente escolar, mudando completamente o seu comportamento. Os pais e professores das vítimas devem ficar atentos a esses tipos de agressões, pois isso gera danos para vida toda.


